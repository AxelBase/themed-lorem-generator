        <h2>The Reversal Algorithm</h2>
        <p>The Yoda theme uses a two-step process. First, it draws from a word pool rich in Star Wars terminology: “force”, “Jedi”, “dark side”, “patience”, “fear”. These establish immediate context. Second, it applies selective syntax transformation to about seventy percent of sentences.</p>

        <p>Rather than blindly reversing all words, the algorithm identifies verb phrases and moves direct objects after them when possible. This creates natural-sounding inversions like “Strong with this one, the force is” instead of complete backward gibberish.</p>

        <h2>Why Partial Reversal Works</h2>
        <p>Full sentence reversal quickly becomes unreadable in long text. But applying transformation selectively preserves the Yoda feel while maintaining flow. Readers recognize the pattern immediately, even in extended paragraphs.</p>

